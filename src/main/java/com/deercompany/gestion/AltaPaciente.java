    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        InsertarPaciente alta = new InsertarPaciente();
        StringBuilder fechaNacimiento = new StringBuilder();
        
        String dni = jtf_dni.getText().trim();
        String obraSocial = jtf_obraSocial.getText().trim().toUpperCase();
        String apellido = jtf_apellido.getText().trim().toUpperCase();
        String nombre = jtf_nombre.getText().trim().toUpperCase();
        String sexo = jtf_sexo.getText().trim().toUpperCase();
        
        String dia = jtf_dia.getText().trim();
        String mes = jtf_mes.getText().trim();
        String anio = jtf_anio.getText().trim();
        
        fechaNacimiento.append(anio).append("-").append(mes).append("-").append(dia);
        
        alta.registrarEntrada(dni, obraSocial, apellido, nombre, sexo, fechaNacimiento.toString());
        limpiarTextField();
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

