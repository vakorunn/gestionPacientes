    private void btn_anadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_anadirActionPerformed
        InsertarTurno alta = new InsertarTurno();
        StringBuilder fechaTurno = new StringBuilder();
        StringBuilder horaTurno = new StringBuilder();
        
        String obraSocial = jtf_obraSocial.getText().trim().toUpperCase();
        String apellido = jtf_apellido.getText().trim().toUpperCase();
        String nombre = jtf_nombre.getText().trim().toUpperCase();
        
        String dia = jtf_dia.getText().trim();
        String mes = jtf_mes.getText().trim();
        String anio = jtf_anio.getText().trim();
        
        fechaTurno.append(anio).append("-").append(mes).append("-").append(dia);
        
        String hora = jtf_hora.getText().trim();
        String minuto = jtf_minutos.getText().trim();
        
        horaTurno.append(hora).append(":").append(minuto).append(":").append("00");
        
        alta.registrarTurno(obraSocial,apellido,nombre,fechaTurno.toString(),horaTurno.toString());
        limpiarTextField();
        this.dispose();
    }//GEN-LAST:event_btn_anadirActionPerformed

